// Напишите IIFE, которая принимает массив и возвращает новый массив, содержащий только
//  уникальные элементы.
//  [1, 1, 2, 2, 2, 3, 4, 5, 5, 5]–> [1, 2, 3, 4, 5].

const arr = [1, 1, 2, 2, 2, 3, 4, 5, 5, 5];


(function (){
    let arr2 = [];
    for (let i = 0; i < arr.length; i++) {
        if(!arr2.includes(arr[i])){
            arr2.push(arr[i]);
        }
        
    }
    console.log(arr2);
    
})()